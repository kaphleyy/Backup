package Ass14;

public class DoubleToIntCasting {
    public static void main(String[] args) {
        double a = 343.4;
        int x = (int) a;
        byte y = (byte) a;
        System.out.println(x + " " + y);
        System.out.println((int)888.8);
        long z = (byte)100L;
        System.out.println(z);
        System.out.println((byte)100L);
        System.out.println((byte)125);
    }
    
}


